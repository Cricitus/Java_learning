package com.werner.branch;

public class SWITCH2 {
    public static void main(String[] args) {
        //表达式类型只能是byte,char,int,string
        //case后不能重复
        //case后的break不能省略
        int a =10;
        switch(a){
            case 10:
                System.out.println('S');
                break;
        }
    }
}
