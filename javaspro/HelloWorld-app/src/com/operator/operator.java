package com.operator;

public class operator {
    public static void main(String[] args) {
        int a=10;
        int b=2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a/b);
        //加号可做连接符
        int a1=2;
        System.out.println(a1+'a'+"idan");//注意这里的a和2直接相加了，所以最好用字符串
    }
}
