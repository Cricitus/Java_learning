package com.operator;

public class operator3 {
    public static void main(String[] args) {
        double a = 9.5;
        double b = 520;
        a+=b;
        System.out.println(a);
        a-=b;
        System.out.println(a);
        a*=b;
        System.out.println(a);
        a/=b;
        System.out.println(a);
        a%=b;
        System.out.println(a);
    }
}
